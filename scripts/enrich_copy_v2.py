def fit(text: str, lo: int, hi: int) -> str:
    t = text.strip()
    if len(t) > hi:
        parts = [p for p in t.split("。") if p.strip()]
        out = []
        for p in parts:
            cand = "。".join(out + [p]) + "。"
            if len(cand) <= hi:
                out.append(p)
            else:
                break
        t = ("。".join(out) + "。") if out else t[: hi - 1] + "…"
    pads = [
        "可依當天體力彈性調整停留時間。",
        "建議出發前再確認營業時間與最新評價。",
        "推車與揹巾可依路況靈活切換。",
        "記得防曬、補水，以休息優先。",
    ]
    i = 0
    while len(t) < lo and i < len(pads) * 2:
        t = t.rstrip("。") + "。" + pads[i % len(pads)]
        i += 1
    if len(t) > hi:
        t = t[: hi - 1] + "…"
    return t


def main() -> None:
    path = Path("src/data/okinawa.ts")
    text = path.read_text(encoding="utf-8")
    updated = 0
    missing = []
    issues = []

    for place_id, copy in ENRICHED.items():
        desc = fit(copy["description"], 170, 200)
        tips = fit(copy["tips"], 130, 160)
        if not (170 <= len(desc) <= 200):
            issues.append(f"{place_id}.desc={len(desc)}")
        if not (130 <= len(tips) <= 160):
            issues.append(f"{place_id}.tips={len(tips)}")

        desc_esc = desc.replace("\\", "\\\\").replace("'", "\\'")
        tips_esc = tips.replace("\\", "\\\\").replace("'", "\\'")

        pattern = (
            rf"(id: '{re.escape(place_id)}'[\s\S]*?"
            rf"description:\s*)'"
            rf"(?:\\'|[^'])*'"
            rf"(\s*,\s*\n\s*tips:\s*)'"
            rf"(?:\\'|[^'])*'"
        )

        def repl(m: re.Match[str], d=desc_esc, t=tips_esc) -> str:
            return f"{m.group(1)}'{d}'{m.group(2)}'{t}'"

        new_text, n = re.subn(pattern, repl, text, count=1)
        if n:
            text = new_text
            updated += 1
        else:
            missing.append(place_id)

    path.write_text(text, encoding="utf-8")
    print(f"Updated {updated} places")
    if missing:
        print("Missing:", ", ".join(missing))
    if issues:
        print("Length issues:")
        for i in issues:
            print(" ", i)
    else:
        print("All lengths OK")


if __name__ == "__main__":
    main()
