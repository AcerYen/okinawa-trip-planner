def char_len(s: str) -> int:
    return len(s)


def main() -> None:
    path = Path("src/data/okinawa.ts")
    text = path.read_text(encoding="utf-8")
    updated = 0
    missing = []
    length_issues = []

    for place_id, copy in ENRICHED.items():
        desc = copy["description"]
        tips = copy["tips"]
        for label, content in [("desc", desc), ("tips", tips)]:
            n = char_len(content)
            if n < 140 or n > 220:
                length_issues.append(f"{place_id}.{label}: {n}")

        desc_esc = desc.replace("\\", "\\\\").replace("'", "\\'")
        tips_esc = tips.replace("\\", "\\\\").replace("'", "\\'")

        pattern = (
            rf"(id: '{re.escape(place_id)}'[\s\S]*?"
            rf"description:\s*)"
            rf"(?:'(?:\\'|[^'])*'|\"(?:\\\"|[^\"])*\"|\n\s*'(?:\\'|[^'])*'(?:\n\s*'(?:\\'|[^'])*')*)"
            rf"(\s*,\s*\n\s*tips:\s*)"
            rf"(?:'(?:\\'|[^'])*'|\"(?:\\\"|[^\"])*\"|\n\s*'(?:\\'|[^'])*'(?:\n\s*'(?:\\'|[^'])*')*)"
        )

        def repl(m: re.Match[str], d=desc_esc, t=tips_esc) -> str:
            return f"{m.group(1)}'{d}'{m.group(2)}'{t}'"

        new_text, n = re.subn(pattern, repl, text, count=1)
        if n:
            text = new_text
            updated += 1
        else:
            missing.append(place_id)

    path.write_text(text, encoding="utf-8")
    print(f"Updated {updated} places")
    if missing:
        print("Missing:", ", ".join(missing))
    if length_issues:
        print("Length warnings (<140 or >220):")
        for issue in length_issues:
            print(" ", issue)
    else:
        print("All lengths within target range")


if __name__ == "__main__":
    main()
