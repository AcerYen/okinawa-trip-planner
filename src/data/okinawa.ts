/** 依景點順序與車程打包當日行程 */
function snapSchedule(min: number, step = 15) {
  return Math.round(min / step) * step
}

function scheduleFromPlaces(
  placeIds: string[],
  dayStartMin = 9 * 60
): ScheduleItem[] {
  const DAY_END = 22 * 60
  let t = dayStartMin
  const items: ScheduleItem[] = []
  let prev: Attraction | undefined

  for (const placeId of placeIds) {
    const place = attractions.find((a) => a.id === placeId)
    if (!place) continue
    if (prev) {
      t = snapSchedule(t + estimateTravelMinutes(prev, place))
    }
    let durationMin = getVisitDuration(place, prev?.id)
    if (t + durationMin > DAY_END) {
      durationMin = Math.max(15, DAY_END - t)
    }
    if (t >= DAY_END - 15) {
      t = Math.max(dayStartMin, DAY_END - 45)
      durationMin = Math.min(durationMin, 45)
    }
    items.push({ placeId, startMin: t, durationMin })
    t += durationMin
    prev = place
  }
  return items
}

function dayPlan(
  day: number,
  title: string,
  placeIds: string[],
  notes: string,
  dayStartMin = 9 * 60
): DayPlan {
  return { day, title, notes, items: scheduleFromPlaces(placeIds, dayStartMin) }
}

